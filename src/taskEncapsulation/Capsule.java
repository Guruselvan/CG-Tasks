package taskEncapsulation;

public class Capsule {
	
	private String data;
	private float digit;
	private long phNo;
	
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public float getDigit() {
		return digit;
	}
	public void setDigit(float digit) {
		this.digit = digit;
	}
	public long getPhNo() {
		return phNo;
	}
	public void setPhNo(long phNo) {
		this.phNo = phNo;
	}
	
	
	
	
	

}
